    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        try{
            Class.forName("com.mysql.jdbc.Driver");
            cn=DriverManager.getConnection("jdbc:mysql://localhost:3306/nsbm_new","root","");
            int row=display_instructor.getSelectedRow();
            String value1=(display_instructor.getModel().getValueAt(row, 0).toString());
            PreparedStatement pt;
            pt = cn.prepareStatement("UPDATE instructor SET ins_id=?,ins_name=? where ins_id=?");
            pt.setString(1, ins_id.getText());
            pt.setString(2, ins_name.getText());
            pt.setString(3, value1);
            pt.executeUpdate();
            DefaultTableModel model =(DefaultTableModel)display_instructor.getModel();
            model.setRowCount(0);
            show_user();
            
            JOptionPane.showMessageDialog(null,"Update sucessfully");
            
        }
        catch(Exception e)
        {   JOptionPane.showMessageDialog(null, e);
    }
    }//GEN-LAST:event_updateActionPerformed

