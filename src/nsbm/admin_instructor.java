    public ArrayList<instructor_user> userList()
    {     ArrayList<instructor_user> userList = new ArrayList();
          try{
            Class.forName("com.mysql.jdbc.Driver");
            cn=DriverManager.getConnection("jdbc:mysql://localhost:3306/nsbm_new","root","");
            String queryl="select *from instructor";
            st=cn.createStatement();
            ResultSet rs=st.executeQuery(queryl);
            instructor_user user;
            while(rs.next())
            {   user=new instructor_user(rs.getString("ins_id"),rs.getString("ins_name"));
                userList.add(user);
                
            
            }
          }
          catch(Exception e)
          {JOptionPane.showMessageDialog(null,e);
          }
          return userList;
    }
    public void show_user()
    {      ArrayList<instructor_user> list = userList();
           DefaultTableModel model =(DefaultTableModel)display_instructor.getModel();
           Object[] row = new Object[2];
           for(int i=0;i<list.size();i++)    
           {    row[0]=list.get(i).getinstructor_id();
                row[1]=list.get(i).getinstructor_name();
                model.addRow(row);
                
           
           
           }
    
    
    
    }

