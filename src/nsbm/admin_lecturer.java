    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        try{
            Class.forName("com.mysql.jdbc.Driver");
            cn=DriverManager.getConnection("jdbc:mysql://localhost:3306/nsbm_new","root","");
            int row=display_lecturer.getSelectedRow();
            String value1=(display_lecturer.getModel().getValueAt(row, 0).toString());
            PreparedStatement pt;
            pt = cn.prepareStatement("UPDATE lecturer SET lec_id=?,lec_name=? where lec_id=?");
            pt.setString(1, lid.getText());
            pt.setString(2, lname.getText());
            pt.setString(3, value1);
            pt.executeUpdate();
            DefaultTableModel model =(DefaultTableModel)display_lecturer.getModel();
            model.setRowCount(0);
            show_user();
            
            JOptionPane.showMessageDialog(null,"Update sucessfully");
            
        }
        catch(Exception e)
        {   JOptionPane.showMessageDialog(null, e);
    }
        
        
        
        
    }//GEN-LAST:event_updateActionPerformed

