     public ArrayList<lecturer_user> userList()
    {     ArrayList<lecturer_user> userList = new ArrayList();
          try{
            Class.forName("com.mysql.jdbc.Driver");
            cn=DriverManager.getConnection("jdbc:mysql://localhost:3306/nsbm_new","root","");
            String queryl="select *from lecturer";
            st=cn.createStatement();
            ResultSet rs=st.executeQuery(queryl);
            lecturer_user user;
            while(rs.next())
            {   user=new lecturer_user(rs.getString("lec_id"),rs.getString("lec_name"));
                userList.add(user);
                
            
            }
          }
          catch(Exception e)
          {JOptionPane.showMessageDialog(null,e);
          }
          return userList;
    }
     public void show_user()
    {      ArrayList<lecturer_user> list = userList();
           DefaultTableModel model =(DefaultTableModel)display_lecturer.getModel();
           Object[] row = new Object[2];
           for(int i=0;i<list.size();i++)    
           {    row[0]=list.get(i).getlecturer_id();
                row[1]=list.get(i).getlecturer_name();
                model.addRow(row);
                
           
           
           }
    
    
    
    }
